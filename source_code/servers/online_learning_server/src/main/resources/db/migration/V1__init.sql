    ADD CONSTRAINT fk_course_requirements_course FOREIGN KEY (course_id) REFERENCES courses (id);

ALTER TABLE registers
    ADD CONSTRAINT fk_registers_course FOREIGN KEY (course_id) REFERENCES courses (id);

ALTER TABLE registers
    ADD CONSTRAINT fk_registers_student FOREIGN KEY (student_id) REFERENCES users (id);

ALTER TABLE objectives
    ADD CONSTRAINT fk_objectives_course FOREIGN KEY (course_id) REFERENCES courses (id);

ALTER TABLE lessons
    ADD CONSTRAINT fk_lessons_course FOREIGN KEY (course_id) REFERENCES courses (id);

ALTER TABLE instructor_statistics
    ADD CONSTRAINT fk_instructor_statistics_instructor FOREIGN KEY (instructor_id) REFERENCES users (id);

ALTER TABLE instructor_payments
    ADD CONSTRAINT uc_instructor_payments_statistic UNIQUE (statistic_id);

ALTER TABLE instructor_payments
    ADD CONSTRAINT fk_instructor_payments_statistic FOREIGN KEY (statistic_id) REFERENCES instructor_statistics (id);

ALTER TABLE courses
    ADD CONSTRAINT fk_courses_category FOREIGN KEY (category_id) REFERENCES categories (id);

ALTER TABLE courses
    ADD CONSTRAINT fk_courses_instructor FOREIGN KEY (instructor_id) REFERENCES users (id);

ALTER TABLE instructor_profiles
    ADD CONSTRAINT fk_instructor_profiles_user FOREIGN KEY (user_id) REFERENCES users (id);

ALTER TABLE lesson_progresses
    ADD CONSTRAINT fk_lesson_progresses_user FOREIGN KEY (user_id) REFERENCES users (id);

ALTER TABLE lesson_progresses
    ADD CONSTRAINT fk_lesson_progresses_lesson FOREIGN KEY (lesson_id) REFERENCES lessons (id);

