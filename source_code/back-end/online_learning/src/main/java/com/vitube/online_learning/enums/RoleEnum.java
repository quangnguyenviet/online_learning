package com.vitube.online_learning.enums;

/**
 * Enum đại diện cho các vai trò trong hệ thống.
 * Bao gồm các vai trò: ADMIN, USER, INSTRUCTOR.
 */
public enum RoleEnum {
    /**
     * Vai trò quản trị viên.
     */
    ADMIN,

    /**
     * Vai trò người dùng thông thường.
     */
    USER,

    /**
     * Vai trò giảng viên.
     */
    INSTRUCTOR;
}